package com.decisionmaking.example.practice;
//Write the program to check whether the no is greater than 100.
public class GreaterThanNum {
	public static void main(String[] args) {
		int num = 60;
		
		if(num > 100) {
			System.out.println("The number "+num+" is greater than 100");
		}else {
			System.out.println("The number "+num+" is smaller than 100");
		}
	}

}
