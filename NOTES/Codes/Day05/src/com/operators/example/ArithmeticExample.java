package com.operators.example;

public class ArithmeticExample {

	public static void main(String[] args) {
		int x = 20;
		int y = 10;
		
		System.out.println("Addition of x = "+x+ " and y = "+y+" = "+( x + y ));
		System.out.println("Subtraction of x = "+x+ " and y = "+y+" = "+( x - y ));
		System.out.println("Multiplication of x = "+x+ " and y = "+y+" = "+( x * y ));
		System.out.println("Division of x = "+x+ " and y = "+y+" = "+( x / y ));
		System.out.println("Modules of x = "+x+ " and y = "+y+" = "+( x % y ));
	}
}
