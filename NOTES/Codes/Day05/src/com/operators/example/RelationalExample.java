package com.operators.example;

public class RelationalExample {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		
		System.out.println(x > y);
		System.out.println(x < y);
		System.out.println(x >= y);
		System.out.println(x <= y);
		System.out.println(x == y);
		System.out.println(x != y);
	}
}
