package com.operators.example;

public class UnaryExample {

	public static void main(String[] args) {
		int a =10;
		
		System.out.println("Increament:");
		a++;//10+1 =11
		System.out.println("a = "+ a);//11
		System.out.println("Decreament:");
		a--;//11-1=10
		System.out.println("a = "+ a);//10
	}
}
